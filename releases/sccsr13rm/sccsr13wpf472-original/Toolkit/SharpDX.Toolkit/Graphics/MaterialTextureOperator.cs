namespace SharpDX.Toolkit.Graphics
{
    /// <summary>
    /// Defines how the Nth texture of a specific type is combined with the result of all previous layers.
    /// </summary>
    public enum MaterialTextureOperator : byte
    {
        /// <summary>
        /// T = T1 * T2 
        /// </summary>
        Multiply,
        /// <summary>
        /// T = T1 + T2
        /// </summary>
        Add,
        /// <summary>
        /// T = T1 - T2
        /// </summary>
        Subtract,
        /// <summary>
        /// T = T1 / T2
        /// </summary>
        Divide,
        /// <summary>
        /// T = (T1 + T2) - (T1 * T2)
        /// </summary>
        SmoothAdd,
        /// <summary>
        /// T = T1 + (T2-0.5)
        /// </summary>
        SignedAdd,
    }
}