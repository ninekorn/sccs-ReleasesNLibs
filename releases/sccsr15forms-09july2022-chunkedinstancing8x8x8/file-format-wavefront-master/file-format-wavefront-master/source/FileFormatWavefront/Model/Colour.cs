namespace FileFormatWavefront.Model
{
    /// <summary>
    /// Represents a colour.
    /// </summary>
    public struct Colour
    {
        /// <summary>
        /// The red component.
        /// </summary>
        public float r;

        /// <summary>
        /// The green component.
        /// </summary>
        public float g;

        /// <summary>
        /// The blue component.
        /// </summary>
        public float b;

        /// <summary>
        /// The alpha component.
        /// </summary>
        public float a;
    }
}